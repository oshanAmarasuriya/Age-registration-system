    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int id_val=(int)all_res_Table.getModel().getValueAt(all_res_Table.getSelectedRow(), 0);
       DefaultTableModel dfm=(DefaultTableModel)all_res_Table.getModel();
        if(all_res_Table.getModel().getValueAt(all_res_Table.getSelectedRow(), 6).toString().equals("")){
             int response= JOptionPane.showConfirmDialog(this,"This record seems to be unmarked yet!.Do you want to delete it?","Are you sure?",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(response==JOptionPane.YES_OPTION){
                db.deleteRow(id_val);
                
                
                m.setRowCount(0);
                fd.getData(m,vall);
                
                m_main.setRowCount(0);
                ffd.getAndFilterData(new Date(), m_main);
                
                dfm.removeRow(all_res_Table.getSelectedRow());

            }
            else if(response==JOptionPane.NO_OPTION){
                
        }
          
        }
        else{
              
            db.deleteRow(id_val);
            m.setRowCount(0);
            fd.getData(m,vall);
            
            m_main.setRowCount(0);
            ffd.getAndFilterData(new Date(), m_main);
            dfm.removeRow(all_res_Table.getSelectedRow());
  
        }

    }//GEN-LAST:event_jButton3ActionPerformed

